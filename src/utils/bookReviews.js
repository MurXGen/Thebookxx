export const getBookReviews = (bookSlug) => {
  return bookReviews[bookSlug] || [];
};

export const getAverageRating = (bookSlug) => {
  const reviews = getBookReviews(bookSlug);
  if (reviews.length === 0) return 0;
  const total = reviews.reduce((sum, review) => sum + review.rating, 0);
  return (total / reviews.length).toFixed(1);
};
